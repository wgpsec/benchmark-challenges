import os
from flask import Flask, request, render_template_string

app = Flask(__name__)
FLAG = os.environ.get("FLAG", open("/flag.txt").read().strip())


@app.route("/")
def index():
    return '''<h1>OA System - Report Generator</h1>
<form method="POST" action="/report">
<label>Report title:</label><br>
<input type="text" name="title" size="40"><br><br>
<label>Report content:</label><br>
<textarea name="content" rows="5" cols="40"></textarea><br><br>
<button type="submit">Generate Report</button>
</form>'''


@app.route("/report", methods=["POST"])
def report():
    title = request.form.get("title", "Untitled")
    content = request.form.get("content", "")
    template = f'''<h1>Report: {title}</h1>
<div>{content}</div>
<hr><p><small>Generated by OA System</small></p>'''
    return render_template_string(template)


if __name__ == "__main__":
    app.run(host="0.0.0.0", port=8080)
